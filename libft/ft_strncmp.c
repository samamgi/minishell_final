#include "libft.h"
#include <unistd.h>
#include <stdio.h>
#include <string.h>

int	ft_strncmp(const char *s1, const char *s2, size_t n)
{
	unsigned long	i;
	int				dif;
	unsigned char	*str1;
	unsigned char	*str2;

	str1 = (unsigned char *)s1;
	str2 = (unsigned char *)s2;
	i = 0;
	while (i < n && (str1[i] != '\0' || str2[i] != '\0'))
	{
		if (str1[i] != str2[i] && (str1[i] != '\0' || str2[i] != '\0'))
		{
			dif = str1[i] - str2[i];
			return (dif);
		}
		i++;
	}
	return (0);
}
